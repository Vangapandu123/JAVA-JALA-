public class StringConcatenation {
    public static void main(String[] args) {
        // Declare two strings
        String str1 = "Hello";
        String str2 = "World";

        // Concatenating using + operator
        String result = str1 + " " + str2;

        // Print the result
        System.out.println("Concatenated String: " + result);
    }
}
